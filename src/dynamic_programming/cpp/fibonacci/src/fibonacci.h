/**
 * @brief Computes the nth Fibonacci number using basic recursion.
 * @param n The index of the Fibonacci number to compute (0-indexed).
 * @return The nth Fibonacci number.
 */
int fibonacciBasic(int n);

/**
 * @brief Computes the nth Fibonacci number using memoization.
 * @param n The index of the Fibonacci number to compute (0-indexed).
 * @return The nth Fibonacci number.
 */
int fibonacciMemo(int n);

/**
 * @brief Computes the nth Fibonacci number using tabulation (bottom-up DP).
 * @param n The index of the Fibonacci number to compute (0-indexed).
 * @return The nth Fibonacci number.
 */
int fibonacciTable(int n);
